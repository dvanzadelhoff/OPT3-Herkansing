public class PassagierPassen {

    public boolean GeldigPaspoort;
    public boolean HasMinorPass;

    public void setGeldigPaspoort(boolean geldigPaspoort)
    {
        GeldigPaspoort = geldigPaspoort;
    }

    public void setHasMinorPass(boolean hasMinorPass)
    {
        HasMinorPass = hasMinorPass;
    }

    public boolean isGeldigPaspoort()
    {
        return GeldigPaspoort;
    }

    public boolean isHasMinorPass()
    {
        return HasMinorPass;
    }
}

