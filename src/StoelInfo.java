public class StoelInfo
{
    private String stoelLabel;
    private String stoelType;

    public void setStoelLabel(String stoelLabel) {
        this.stoelLabel = stoelLabel;
    }

    public void setStoelType(String stoelType) {
        this.stoelType = stoelType;
    }

    public String getStoelType() {
        return stoelType;
    }

    public String getStoelLabel() {
        return stoelLabel;
    }
}
